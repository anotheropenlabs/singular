export type Language = 'en' | 'zh';
export type I18nKey = string;

import { create } from 'zustand';
import { persist } from 'zustand/middleware';

interface I18nStore {
    language: Language;
    setLanguage: (lang: Language) => void;
    t: (key: string, defaultValue?: string) => string;
}

// Simple accessor for nested keys
const getNested = (obj: any, path: string) => {
    return path.split('.').reduce((acc, part) => acc && acc[part], obj);
};

import { useState, useEffect } from 'react';

// Base store with persistence
const useI18nStore = create<I18nStore>()(
    persist(
        (set, get) => ({
            language: 'zh', // Default to Chinese
            setLanguage: (lang) => set({ language: lang }),
            t: (key, defaultValue) => {
                const lang = get().language;
                const text = getNested(resources[lang], key);
                return text || defaultValue || key;
            },
        }),
        {
            name: 'singular-i18n',
        }
    )
);

// Hook to handle hydration mismatch
export const useI18n = () => {
    const store = useI18nStore();
    const [isMounted, setIsMounted] = useState(false);

    useEffect(() => {
        setIsMounted(true);
    }, []);

    // During SSR and first client render, force default language (zh) 
    // to match server markup causing no hydration mismatch
    const activeLanguage = isMounted ? store.language : 'zh';

    const t = (key: string, defaultValue?: string) => {
        const text = getNested(resources[activeLanguage], key);
        return text || defaultValue || key;
    };

    return {
        ...store,
        language: activeLanguage,
        t,
    };
};
